package partei;

/**
 *
 * @author Claudio Díaz
 */
public enum TipoLicencia {
    /**
     * Clase A1: para conducir taxis
     */
    A1, 
    /**
     * Clase A2: para manejar taxis, ambulancias o vehículos motorizados de transporte público y privados de personas con capacidad de 10 a 17 personas, sin contar al conductor.
     */
    A2, 
    /**
     * Clase A3: para conducir los mismos vehículos que la licencia A2, pero sin límites de asientos.
     */
    A3,
    /**
     * Clase A4: para manejar transporte de carga cuyo peso bruto sea superior a 3.500 kg.
     */
    A4,
    /**
     * Clase A5: para conducir todo tipo de vehículos de carga, simples o articulados, cuyo peso bruto sea superior a 3.500 kg.
     */
    A5,
    /**
     * Clase B: para manejar vehículos motorizados de tres o cuatro ruedas para el transporte particular de personas, con capacidad de hasta nueve asientos sin contar al conductor, o de carga y cuyo peso vehicular sea de hasta 3.500 kg.
     */
    B,
    /**
     * Clase C: para vehículos de dos o tres ruedas con motor fijo o agregado, como por ejemplo las motocicletas.
     */
    C,
    /**
     * Clase D: para maquinaria automotriz, como por ejemplo tractores, palas mecánicas o grúas.
     */
    D,
    /**
     * Clase E: coches de tracción animal.
     */
    E,
    /**
     * Clase F: para vehículos motorizados de las Fuerzas Armadas, policiales o bomberos.
     */
    F
}
